<h2>Les épreuves de sprint</h2>
<p>
    <ul>La vitesse individuelle</ul> a vitesse sur piste, également dénommée sprint d'après le terme anglais, est une épreuve qui se court entre deux coureurs en opposition directe. Le départ est donné arrêté et les coureurs sont côte-à-côte sur la ligne de départ. Un tirage au sort définit lequel des deux partira en bas de la piste. La distance à parcourir est de deux ou trois tours de piste suivant la longueur de la piste. Le but est de passer la ligne d'arrivée le premier.

Dans certains cas, l'épreuve peut être courue à trois, quatre ou plus exceptionnellement. C'est notamment le cas pour les repêchages ou pour déterminer le bas des classements.

La tactique de course est très importante car celui qui est en tête peut se faire dépasser dans la ligne du fait de l'aspiration. L'ordre de départ est tiré au sort. Le premier tour est tactique et se fait habituellement à une allure modérée pour observer l'adversaire. Pour éviter la première place au moment du lancement final, il est admis que les deux coureurs effectuent du « surplace » dans une zone délimitée pour obliger l'autre à passer devant.

Le professionnel le plus titré est le japonais Kōichi Nakano avec dix titres.

L'épreuve est pratiquée par les femmes au championnat du monde depuis 1988. 
    <ul>La vitesse par équipes</ul> Deux équipes de trois coureurs s'affrontent sur trois tours de piste. Le départ est donné arrêté et chacune des équipes démarre sur les lignes diamétralement opposées du vélodrome appelées points de poursuite.

Chaque coureur effectue un tour en tête pour entraîner ses coéquipiers puis s'écarte. Lors du dernier tour, seul le dernier relayeur est présent. Son passage sur la ligne donne le temps de l'équipe. Le classement final est donné par ordre croissant de temps.

Dans le cas des courses féminines, seules deux cyclistes par équipe sont au départ. 
</p>
<h2>Les épreuves d'endurance</h2>
<p>
<ul>La poursuite individuelle</ul> La poursuite individuelle est une épreuve mettant en opposition deux coureurs sur 4 000 mètres, ou 3 000 mètres pour les femmes, avec un départ arrêté. Au début de la course les coureurs sont placés sur les lignes diamétralement opposées de la piste, au milieu de chaque ligne droite. L'objectif de l'épreuve est de rattraper son adversaire, le coureur gagnant est donc celui qui arrive à dépasser son adversaire ou qui termine en premier les quatre kilomètres.

Lors des manches de qualification, le temps des coureurs est pris en compte pour déterminer les huit coureurs autorisés à participer au tableau final mais également pour déterminer les faces-à-faces.

L'épreuve est pratiquée par les féminines depuis 1958. 
<ul>La poursuite par équipes</ul> La poursuite par équipes est une épreuve du même principe que la poursuite individuelle. La différence provient du fait que la course est réalisée par des équipes de quatre coureurs se relayant. Les relais sont autorisés tous les demi-tours ou tous les tours. L'intérêt est de conserver le plus longtemps possible les quatre coureurs de l'équipe, dans le but de profiter de l'aspiration qui permet de récupérer de l'effort entre deux passages de relais. Or, le temps de l'équipe étant pris sur le troisième coureur, il est possible qu'un des concurrents défaillant sur la fin du parcours ne termine pas le relais. Le classement final est établi par équipe et par ordre de temps d'arrivée.

Cette épreuve a longtemps été réservée aux hommes, mais depuis 2008 elle a été ouverte aux femmes.</p>

