<h2>Compétition de free-running ou freerun</h2>
<p>Des compétitions de freerun ont été organisées par Red Bull, les Red Bull Art Of Motion. Les meilleurs free runners du monde entier viennent à cette compétition comme Pedro Salgado ou Alfred Scott. Chacun représente en quelque sorte son pays. Ils doivent d'abord effectuer une vidéo pour montrer leurs compétences afin de se qualifier pour la compétition.</p>

<h2>Techniques</h2>
<p>La plupart des techniques dites de free-running sont fondées sur la gymnastique, comme le salto arrière pour ne citer qu’un exemple. Selon chaque pratiquant les appellations peuvent différer. Les noms des acrobaties ne sont donc pas fixes, elles varient en fonction de ceux qui les utilisent. Cependant certaines techniques semblent provenir directement de la communauté du freerun, inspirées par l’environnement extérieur : 
<li>Monkey gainer</li> combinaison d'un saut de chat et d'un salto arrière.
<li>Cast bomb</li> salto arrière effectué à partir d'un appui sur les mains, d'un mur ou d'une barrière.
<li>Kick the moon/Cheat Gainer</li> salto arrière effectué par le lancement de son pied vers l'avant (autre que l'appui).
<li>Tracasser </li> salto avant en se déplaçant vers l'arrière.
<li>Gainer</li> salto arrière en se déplaçant vers l'avant.
<li>Webster</li> salto avant effectué par le lancement de son pied vers l'arrière.
<li>Wall spin</li> salto costal effectué à partir d'un appui des mains sur un mur.
</p>
<h2>Finalité du parkour et valeurs</h2>
<p>Les motivations des traceurs sont similaires aux pratiquants d’autres sports, particulièrement des sports extrêmes : développement physique, aspect communautaire, plaisir, connaissance et dépassement de soi, performance, passion… Ainsi le parkour peut être considéré comme une méthode d’entraînement, de développement physique ou un loisir sportif. De plus les traceurs évoquent des raisons spécifiques au parkour : 
    <li>Le parkour est dans la continuation naturelle des jeux d’enfants. Un traceur a la même attitude que les enfants dans les cours de récréation qui aiment courir, sauter et grimper partout sans se demander pourquoi.</li>
    <li>Le parkour, qui n’est pas codifié comme la plupart des sports, est une forme de liberté. Il permet de franchir des obstacles censés barrer le passage et accéder à des endroits a priori inaccessibles. Certains traceurs font le parallèle entre le franchissement d’obstacles physiques et le fait de surmonter les obstacles de la vie quotidienne.</li>
    <li>Le parkour est utile au-delà du simple développement physique. Les mouvements techniques permettent un déplacement efficace en cas de poursuite, de fuite, de sauvetage, ou simplement une bonne réception en cas de chute. Ainsi les techniques du parkour sont employées par les pompiers de Paris. Les traceurs qui cherchent l’utilité citent la devise de Georges Hébert « être fort pour être utile ».</li>
</p>

