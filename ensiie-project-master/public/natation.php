<h2>Règles de base</h2>
<p>En natation synchronisée les points récompensent la technique, le sens artistique et la difficulté .

Il existe des compétitions solo, duo, en équipes de huit nageuses ou de dix nageuses dans un ballet « combiné ». Les ballets durent entre deux et cinq minutes, selon la catégorie d'âge et de l'épreuve. Une marge de 15 secondes est autorisée avant ou après. Les duos ont droit à une remplaçante, une équipe et un combiné peuvent en avoir deux.

Les nageuses ont le droit de présenter une danse sur la plage, avant le démarrage de leur musique, mais elles ne doivent pas excéder dix secondes.

Les nageuses n'ont en aucun cas le droit de toucher le fond de la piscine, même lorsqu'elles effectuent des portés (lorsqu'une voltigeuse saute au-dessus de l'eau grâce à la propulsion que lui donnent ses coéquipières). Lors des compétitions importantes, une caméra sous-marine vérifie qu'aucune nageuse ne s'aide du fond, sinon l'équipe obtient une pénalité.

Une compétition se déroule en deux parties : </p>
<h2>Les figures imposées : </h2>
<p><li>Benjamines, minimes : elles comportent quatre figures imposées (deux obligatoires et deux tirées au sort), réalisées de manière statique et sans musique devant un jury ;</li>
<li>Juniors et seniors : les nageuses exécutent des éléments imposés (qui sont les mêmes pour tous les clubs et/ou nations), dans un ordre donné et harmonisés durant leur chorégraphie qu'elles peuvent réaliser en solo, duo ou en équipe. On les appelle « épreuves techniques ».</li></p>
<h2>Le programme libre :</h2>
<p>    les entraîneurs créent leur propre programme (solo, duo, équipe), parfois à l'aide de la créativité des nageuses, sur une durée imposée.

Les positions de base principales sont la « queue de poisson » (ou « flamant rose »), la « verticale », la « jambe de ballet », la « verticale jambe pliée », le « château », etc.7.

Pendant l'exécution des programmes, il est interdit de toucher le fond de la piscine ou le bord du bassin. Les mouvements peuvent être effectués avec les jambes ou les bras, sous l'eau ou hors de l'eau. Il est possible de faire des « portés ». Toutes les nageuses doivent évoluer de façon synchronisée entre elles et avec la musique.

Les juges doivent noter le travail technique pour les figures et les aspects techniques, artistiques et la difficulté pour les programmes. Les notes techniques sont données pour la synchronisation et l'exécution des mouvements. Les notes artistiques sont données pour la chorégraphie, l'interprétation musicale et le style de présentation. Les notes de difficulté sont données pour la difficulté de la chorégraphie (position rapprochée entre les nageuses, longueur des figures, rapidité d'exécution, mouvements difficiles, etc.)</p>
<p>La tenue autorisée se compose :
<li>D'un pince-nez ; qui empêche l'eau de rentrer dans le nez lorsque les nageuses ont la tête « à l'envers », c'est-à-dire surtout lors des figures. Les pince-nez sont autorisés en toutes circonstances.</li>
<li>De lunettes de natation ; les lunettes ne sont autorisées que pour les séances d'entraînement et les figures imposées. Elles ne sont pas permises lors des ballets en compétition.</li>
<li>D'un bonnet de bain ; autorisé pour les séances d'entraînement et les figures imposées. Les figures imposées s'exécutent en maillot de bain noir et en bonnet blanc (au Québec, il s'agit d'un maillot de bain noir et d’un bonnet de l'équipe dont les nageuses font partie pour l’entrainement et d'un maillot de bain noir et d’un bonnet blanc pour les figures imposées) afin que les nageuses ne soient pas reconnaissables par le jury lors des compétitions.</li>
<li>D'un maillot de bain, couleurs au choix pour les séances d'entraînement, noir pour les figures imposées et en accord avec le thème musical pour les programmes. Les maillots de bain de compétition sont beaucoup plus travaillés : il peut y avoir différents tissus, de différentes couleurs, des paillettes et des motifs rappelant la musique. Les maillots de bain doivent être d'une pièce, sans trop d'échancrure (au-dessus des hanches maximum) ni de décolleté. Les accessoires ne sont pas permis.</li>
Par ailleurs, les coiffes (uniquement pour les programmes), en harmonie avec les maillots, sont tenues par des pinces, autour d'un chignon, sur des cheveux gélatinés (avec de la gélatine alimentaire) pour une bonne tenue dans l'eau. Le maquillage est en harmonie avec les couleurs du maillot et le vernis à ongles est autorisé uniquement pour les compétitions internationales, à condition que toutes les nageuses aient le même. Le port de bijoux est interdit et les tatouages doivent être couverts.
</p>

