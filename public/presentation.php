                        <h3>Les sites de formation</h3>
                        <p>Les étudiants en PACES sont répartis sur 2 sites de formation : <strong>le campus universitaire d’Orsay et la faculté de pharmacie de Châtenay Malabry</strong>. Le choix du lieu d’études est laissé à la volonté des étudiants lors de l’affectation PARCOURSUP (dans la mesure des capacités d’accueil).</p>
                        <br/>
                        <p>À l'issue de cette première année d’études de santé, <strong>les étudiants reçus peuvent être amenés à changer de lieu d’études</strong>. En effet, pour la filière médecine, la faculté se situe au Kremlin Bicêtre; en pharmacie, les études se poursuivent au sein de la faculté de Châtenay Malabry; en odontologie, les sites de formation sont les facultés de chirurgie dentaires de Montrouge et Garancière; l’école de maïeutique est à l’hôpital Foch et l’école de kinésithérapie (ENKRE) est à Saint Maurice.
                        </p>
