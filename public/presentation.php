                        <h3>Nos initiatives pédagogiques</h3>
                        <div>
                            <h4>Pré-rentrée</h4>
                            <p>Tous les ans, deux semaines avant la rentrée universitaire officielle, le TSPS organise un <strong>stage de pré-rentrée</strong> dans les locaux du site d'Orsay. Le but de cette pré-rentrée est de mettre l’étudiant de PACES dans un <strong>rythme</strong> et une <strong>ambiance</strong> qui sont similaires à ceux de l'université, avec une cadence de cours en amphithéâtre le matin et de TD l’après-midi assez intense pour pouvoir balayer une bonne partie du programme dans toutes les matières du premier semestre.</p>
                            <p>L’objectif premier de cette pré-rentrée est de donner aux étudiants une <strong>base solide de connaissances</strong> et des <strong>conseils méthodologiques</strong> pour pouvoir mieux assimiler les cours pendant l’année.</p>
                            <p>Pour clôturer le stage, un <strong>Concours Blanc Général</strong> a lieu, dans les salles de concours, portant sur l’ensemble de ce qui a été traité durant les deux semaines, aboutissant à un <strong>classement</strong>. Ce stage permet donc de se familiariser à la fois avec les locaux, le programme et le mode de travail attendu.</p>
                        </div><br/>
                        <div>
                            <h4>Concours blancs</h4>
                            <p><strong>Tout au long de l’année</strong>, deux fois par semaine, après les cours, des <strong>Concours Blancs</strong> sont organisés dans les amphis. Ceux-ci, d’une durée similaire à celle du Concours et <strong>au format de l’épreuve</strong>, permettent aux étudiants de s’entraîner sur des sujets rédigés par notre équipe et relus par les professeurs de PACES afin qu'ils soient les <strong>plus représentatifs possibles</strong>. À la fin de l’épreuve, une correction est distribuée et les différents étudiants sont notés et classés anonymement sur l’ensemble des participants.</p>
                            <p><strong>En fin de semestre</strong>, un <strong>Concours Blanc Général</strong> a lieu, sur le site d’Orsay, dans les salles de Concours, en temps et format réels. Les étudiants bénéficient donc d’une <strong>« répétition générale »</strong> du jour J environ 15 jours avant celui-ci. La participation sur cette journée étant de plus de 700 étudiants, le <strong>classement publié in fine</strong> permet aux étudiants de se situer par rapport à sa promotion de manière assez représentative.</p>
                        </div><br/>
                        <div>
                            <h4>Carnets du tuto</h4>
                            <p>Afin de combler la prise de note des étudiants, chaque cours est suivi par des tuteurs, qui rédigent ensuite des <strong>fiches de cours</strong> mises en ligne sur <strong>Dokeos</strong> (plateforme en ligne de l’Université) et sur notre site.</p>
