                        <h3>Les enseignements</h3>
                        <p>Au premier semestre, les <strong>cours magistraux</strong> (CM), sur le site d’Orsay, ont lieu le matin, permettant aux <strong>travaux dirigés</strong> (TD) d’avoir lieu l’après-midi. Ceci est inversé à Châtenay Malabry, où les TD sont faits le matin et les cours magistraux donnés l’après-midi. Les différents professeurs donnent ainsi leur cours une première fois le matin, puis le même une seconde fois l’après-midi. Au second semestre, les cours magistraux n’ont lieu que l’après-midi sur les deux sites. Ainsi, le professeur alterne sa présence sur les deux lieux et le cours est <strong>vidéo transmis en direct</strong> sur le second endroit. Cette répartition permet aux étudiants de recevoir le même enseignement peu importe le site et de permettre <strong>l’équité face au Concours</strong> qui est le même pour tous.
                        </p><br/>
                        <p>Les matières sont réparties par <strong>Unités d’Enseignement</strong> (UE). Ainsi, au <strong>premier semestre</strong> sont enseignées:
                        <ul>
                            <li>une UE de chimie-biochimie (UE1)</li>
                            <li>une UE de biologie cellulaire, embryologie et histologie (UE2)</li>
                            <li>une UE de physique-biophysique (UE3.a)</li>
                            <li>une UE de Biostatistiques (UE4)</li>
                        </ul>
                        </p><br/>
                        <p>Au <strong>second semestre</strong>, l’UE de physique-biophysique est conservée, avec en plus une UE d’anatomie (UE5), une d’initiation à la connaissance du médicament (UE6), et une de Santé, Société et Humanité (UE7). <strong>Deux UE spécifiques</strong> sont également enseignées à titre obligatoire, réparties en <strong>trois différents modules</strong>:
                        </p>
                        <ul>
                            <li>L’UES1 (UE d’anatomie), pour lequel le module A comprend de l’Anatomie et histologie de l’appareil reproducteur et du sein ; le module B, de l’anatomie de la tête et du cou ; et le module C, de l’anatomie du petit bassin.</li>
                            <li>L'UES2 (UE relative à la conception du médicament) pour lequel le module D traite des bases physico chimiques des principes actifs ; le module E, de la conception, de la formulation, de la production et du contrôle des médicaments ; et le module F, des sources actuelles et futures de principes actifs.</li>
